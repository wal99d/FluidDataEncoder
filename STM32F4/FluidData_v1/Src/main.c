void DumpHex(const void* data, size_t size) {
	char ascii[17];
	size_t i;
	ascii[16] = '\0';
	for (i = 0; i < size; ++i) {
		if (((unsigned char*)data)[i] >= ' ' && ((unsigned char*)data)[i] <= '~') {
			ascii[i % 16] = ((unsigned char*)data)[i];
		} else {
			ascii[i % 16] = '.';
		}
		if ((i+1) % 8 == 0 || i+1 == size) {
			    if (i+1 == size) {
				ascii[(i+1) % 16] = '\0';
				sprintf(aTxBuffer,"%s\r\n", ascii);
				 HAL_UART_Transmit(&huart1, aTxBuffer , sizeof(aTxBuffer)/sizeof(*aTxBuffer), 1000);
			}
		}
	}
}
