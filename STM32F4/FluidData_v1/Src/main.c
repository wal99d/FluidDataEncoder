void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  if(GPIO_Pin == GPIO_PIN_1)
  {

	   if(TIMER==0){
		   //Start TIM4
	     HAL_TIM_Base_Start_IT(&htim4); // start timer interrupts
		 HAL_NVIC_SetPriority(TIM4_IRQn, 0, 1);
		 HAL_NVIC_EnableIRQ(TIM4_IRQn);
	   }
	   COUNT++;
  }
}


